def random_relation(
    carrier: Iterable[Any],
    seed: int | None = None,
    density: float = 0.5,
) -> set[tuple[Any, Any]]:
    """Return a random binary relation on *carrier*.

    Each pair (x, y) in carrier × carrier is included independently with
    probability *density*.

    Parameters
    ----------
    carrier:
        Elements of the underlying set.
    density:
        Inclusion probability per pair, in [0.0, 1.0] (default 0.5).
    seed:
        Random seed for reproducibility.

    Examples
    --------
    >>> random_relation([1, 2, 3], density=1.0)
    {(1,1),(1,2),(1,3),(2,1),(2,2),(2,3),(3,1),(3,2),(3,3)}
    """
    if not (0.0 <= density <= 1.0):
        raise RandomGeneratorError(f"density must be in [0.0, 1.0], got {density}.")
    rng = Random(seed)
    carrier_list = list(carrier)
    relation: set[tuple[Any, Any]] = set()
    for x in carrier_list:
        for y in carrier_list:
            if rng.random() < density:
                relation.add((x, y))
    return relation


