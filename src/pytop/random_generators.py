def random_function(
    domain: Iterable[Any],
    codomain: Iterable[Any],
    seed: int | None = None,
) -> dict[Any, Any]:
    """Return a random function from *domain* to *codomain* as a dict.

    Each domain element is assigned a uniformly random codomain element.
    Pass the result to :func:`pytop.maps.make_function` to obtain a
    :class:`~pytop.maps.FiniteMap`.

    Parameters
    ----------
    domain:
        Elements of the domain set.
    codomain:
        Elements of the codomain set (must be non-empty).
    seed:
        Random seed for reproducibility.
    """
    rng = Random(seed)
    domain_list = list(domain)
    codomain_list = list(codomain)
    if not codomain_list:
        raise RandomGeneratorError("Codomain must be non-empty.")
    return {x: rng.choice(codomain_list) for x in domain_list}


