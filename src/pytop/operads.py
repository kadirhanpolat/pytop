def is_koszul_operad(space: Any) -> Result:
    """Check whether the operad is Koszul.

    An augmented quadratic operad P is Koszul if the Koszul complex
    P^! circ_kappa P is acyclic (quasi-isomorphic to the unit operad I).
    Equivalently, the bar construction B(P) is quasi-isomorphic to the
    Koszul dual cooperad P^!. Key Koszul operads: Ass, Com, Lie, E_n.

    Decision layers
    ---------------
    1. Explicit Koszul tags -> true.
    2. Known Koszul operads (Ass, Com, Lie, E_n by name/tag) -> true.
    3. Non-quadratic or known non-Koszul operads -> false.
    4. Unknown otherwise.
    """
    tags = _extract_tags(space)
    representation = _representation_of(space)
    base: dict[str, Any] = {"representation": representation, "tags": sorted(tags)}

    if _matches_any(tags, KOSZUL_DUALITY_TAGS):
        witness = next(t for t in tags if t in KOSZUL_DUALITY_TAGS)
        return Result.true(
            mode="theorem",
            value="koszul_operad",
            justification=[
                f"Tag {witness!r}: the operad is Koszul — the Koszul complex "
                "P^! circ_kappa P is acyclic, giving the minimal free resolution "
                "P_infty = Omega(P^!) of P.",
            ],
            metadata={**base, "criterion": "explicit_koszul", "witness": witness},
        )

    known_koszul = {
        "assoc_operad", "ass_operad", "comm_operad", "com_operad",
        "lie_operad", "lie_algebra_operad",
        "little_disks_operad", "e_n_operad", "e2_operad", "e1_operad",
        "poisson_operad",
    }
    if _matches_any(tags, known_koszul | ASSOC_OPERAD_TAGS | COMM_OPERAD_TAGS | LIE_OPERAD_TAGS):
        relevant = (known_koszul | ASSOC_OPERAD_TAGS | COMM_OPERAD_TAGS | LIE_OPERAD_TAGS) & tags
        witness = next(iter(relevant))
        return Result.true(
            mode="theorem",
            value="koszul_operad",
            justification=[
                f"Tag {witness!r}: Ass, Com, and Lie are Koszul operads "
                "(Ginzburg-Kapranov 1994). Little n-disks E_n are Koszul for all n. "
                "Their Koszul resolutions give A_infty, C_infty, and L_infty algebras.",
            ],
            metadata={**base, "criterion": "known_koszul_operad", "witness": witness},
        )

    non_koszul = {
        "non_koszul", "non_quadratic_operad", "not_koszul",
        "non_binary", "arity_greater_than_2_only",
    }
    if _matches_any(tags, non_koszul):
        witness = next(t for t in tags if t in non_koszul)
        return Result.false(
            mode="theorem",
            value="not_koszul",
            justification=[
                f"Tag {witness!r}: the operad is not Koszul. "
                "Non-binary or non-quadratic operads cannot be Koszul in the classical "
                "sense (Koszulness requires a binary quadratic presentation).",
            ],
            metadata={**base, "criterion": "explicit_non_koszul", "witness": witness},
        )

    return Result.unknown(
        mode="symbolic",
        value="unknown",
        justification=[
            "No tags indicate Koszulness or its failure. "
            "Cannot determine whether the operad is Koszul.",
        ],
        metadata={**base, "criterion": "insufficient_data"},
    )


def has_infinity_algebra_structure(space: Any) -> Result:
    """Check whether the space admits a homotopy algebra structure.

    A P_infty-algebra (A_infty, L_infty, C_infty, E_infty, ...) is an algebra over
    the minimal resolution P_infty = Omega(P^!) of a Koszul operad P. Key cases:
    A_infty (homotopy associative), L_infty (homotopy Lie), E_infty (commutative
    up to all higher homotopy coherence).

    Decision layers
    ---------------
    1. Explicit infinity-algebra tags (a_infinity, l_infinity, ...) -> true.
    2. Bar-cobar construction tags (which produce minimal resolutions) -> true.
    3. Strictly algebras over Koszul operads (which have infinity versions) -> true.
    4. Strict (non-homotopy) only tags -> false.
    5. Unknown otherwise.
    """
    tags = _extract_tags(space)
    representation = _representation_of(space)
    base: dict[str, Any] = {"representation": representation, "tags": sorted(tags)}

    if _matches_any(tags, INFINITY_ALGEBRA_TAGS):
        witness = next(t for t in tags if t in INFINITY_ALGEBRA_TAGS)
        return Result.true(
            mode="theorem",
            value="infinity_algebra",
            justification=[
                f"Tag {witness!r}: the space is a homotopy algebra — "
                "operations satisfying the classical axioms only up to coherent homotopy. "
                "The classical algebra structure is a special case with higher operations = 0.",
            ],
            metadata={**base, "criterion": "explicit_infinity_algebra", "witness": witness},
        )

    if _matches_any(tags, BAR_COBAR_TAGS):
        witness = next(t for t in tags if t in BAR_COBAR_TAGS)
        return Result.true(
            mode="theorem",
            value="infinity_algebra",
            justification=[
                f"Tag {witness!r}: bar-cobar constructions produce P_infty-algebra "
                "structures. The cobar construction Omega(C) of a cooperad C models "
                "P_infty-algebras as Maurer-Cartan elements in the bar complex.",
            ],
            metadata={**base, "criterion": "bar_cobar_construction", "witness": witness},
        )

    koszul_strict = {
        "associative_algebra", "commutative_algebra", "lie_algebra_operad",
        "assoc_operad", "comm_operad", "lie_operad",
    }
    if _matches_any(tags, koszul_strict | ASSOC_OPERAD_TAGS | COMM_OPERAD_TAGS | LIE_OPERAD_TAGS):
        relevant = (koszul_strict | ASSOC_OPERAD_TAGS | COMM_OPERAD_TAGS | LIE_OPERAD_TAGS) & tags
        witness = next(iter(relevant))
        return Result.true(
            mode="theorem",
            value="infinity_algebra",
            justification=[
                f"Tag {witness!r}: every strict Ass/Com/Lie-algebra is trivially an "
                "A_infty/C_infty/L_infty-algebra (with all higher operations m_n = 0 for n>=3). "
                "The homotopy transfer theorem then gives a genuine P_infty-structure "
                "on any homotopy retract.",
            ],
            metadata={**base, "criterion": "strict_implies_infinity", "witness": witness},
        )

    strict_only = {"strict_algebra_only", "no_higher_operations", "not_homotopy_algebra"}
    if _matches_any(tags, strict_only):
        witness = next(t for t in tags if t in strict_only)
        return Result.false(
            mode="theorem",
            value="no_infinity_structure",
            justification=[
                f"Tag {witness!r}: the algebra is strictly classical — no higher "
                "homotopy operations. It does not admit a non-trivial P_infty-structure.",
            ],
            metadata={**base, "criterion": "strict_only", "witness": witness},
        )

    return Result.unknown(
        mode="symbolic",
        value="unknown",
        justification=[
            "No tags indicate a homotopy algebra structure or its absence. "
            "Cannot determine P_infty-algebra status.",
        ],
        metadata={**base, "criterion": "insufficient_data"},
    )


