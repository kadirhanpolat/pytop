r"""Operads: symmetric/non-symmetric operads, Koszul duality, bar-cobar constructions,
A-infinity/L-infinity/E-infinity algebras, little disks operads, tree compositions.

Key theorems and constructions implemented
------------------------------------------
- Operad (symmetric): a symmetric operad P is a collection {P(n)}_{n>=0} of S_n-modules
  (right modules over the symmetric group S_n) together with composition maps:
  gamma: P(k) tensor P(n_1) tensor ... tensor P(n_k) -> P(n_1 + ... + n_k),
  a unit 1 in P(1), subject to associativity, unity, and equivariance axioms.
  Equivalently, P is a monoid in the category (Seq, tensor_circ, I) of symmetric sequences
  with respect to the composition product: (P circ Q)(n) = direct_sum_{k>=0} P(k)
  tensor_{S_k} Q^{tensor k}(n). An algebra over P is a vector space (or object) A
  together with maps P(n) tensor_{S_n} A^{tensor n} -> A satisfying unit and
  associativity with respect to the operad structure. Key examples:
  - Ass: the associative operad with Ass(n) = k[S_n] (free S_n-module of rank 1);
    Ass-algebras are exactly associative algebras.
  - Com: the commutative operad with Com(n) = k (trivial S_n-module of rank 1);
    Com-algebras are exactly commutative associative algebras.
  - Lie: the Lie operad encoding the Lie bracket; Lie-algebras are Lie algebras.
  Non-symmetric (planar) operad: same as above but without the S_n-action; algebras
  are algebras with ordered inputs. Non-symmetric Ass encodes associative algebras;
  non-symmetric Com does not make sense (commutativity requires the transposition).

- Koszul duality for operads (Ginzburg-Kapranov, 1994): a binary quadratic operad P
  (generated by binary operations with quadratic relations) has a Koszul dual operad P^!.
  A quadratic operad P = (E, R) is presented by generators E in arity 2 and quadratic
  relations R in E circ E. The Koszul dual: P^! = (E^*, R^\perp) where E^* = Hom(E, k)
  with twisted S_2-action, and R^\perp is the orthogonal complement of R under the
  natural pairing. Fundamental examples:
  - Com^! = Lie, Lie^! = Com (Com and Lie are Koszul dual to each other).
  - Ass^! = Ass (associative operad is self-dual up to twist).
  Koszul operads: P is Koszul if the augmentation epsilon: B(P^!) -> k is a
  quasi-isomorphism, where B denotes the bar construction. Equivalently, the Koszul
  complex P^! circ_kappa P is acyclic (quasi-isomorphic to the unit operad I).
  Consequence: if P is Koszul, then P^{infty} = Omega(P^!) (bar-cobar construction)
  is the minimal resolution of P, whose algebras are P-algebras up to homotopy
  (P_infty-algebras). The main theorem: Ass, Com, and Lie are all Koszul operads.

- Bar-cobar constructions: for an augmented operad P (with augmentation P -> I), the
  bar construction B(P) is a cooperad (graded, with a differential making it a dg-cooperad)
  with B(P) = T^c(s P_+) (cofree cooperad on the suspension of the augmentation ideal).
  The cobar construction Omega(C) of an augmented cooperad C is the operad
  Omega(C) = T(s^{-1} C_+) with differential induced by the coproduct of C.
  Adjunction: (Omega, B) form an adjoint pair between dg-operads and dg-cooperads.
  The counit Omega(B(P)) -> P is a quasi-isomorphism for augmented operads (always),
  and the unit P^! -> B(P)/relations is a quasi-isomorphism iff P is Koszul.
  Applications: the bar construction B(P) is used to define P-infinity algebras
  (via their Maurer-Cartan elements), and the Koszul complex is the twisted composite.

- A-infinity algebras (Stasheff, 1963): an A-infinity algebra (A_infty-algebra) is a
  graded vector space A with a collection of multilinear maps {m_n: A^{tensor n} -> A}_{n>=1}
  of degree 2-n satisfying the A_infty-relations:
  sum_{n=r+s+t} (-1)^{rs+t} m_{r+1+t}(id^r tensor m_s tensor id^t) = 0 for all n>=1.
  For n=1: m_1 circ m_1 = 0 (m_1 is a differential);
  for n=2: m_1 m_2 = m_2(m_1 tensor 1 + 1 tensor m_1) (derivation condition);
  for n=3: the failure of m_2 to be associative is controlled by m_3 (homotopy).
  If m_n = 0 for n>=3: A_infty reduces to a dg-algebra (differential graded associative).
  If additionally m_1 = 0: A_infty reduces to a graded associative algebra.
  A_infty-algebras are the algebras over the A_infty operad = Omega(Ass^!).
  Homotopy transfer theorem: any A_infty algebra structure on a complex (A, d) can be
  transferred to any homotopy retract of A, producing an A_infty structure on the
  cohomology H^*(A). This is fundamental in homological perturbation theory.

- L-infinity algebras: an L_infty-algebra (L_infty = strong homotopy Lie algebra) is a
  graded vector space L with maps {l_n: Lambda^n L -> L}_{n>=1} (alternating, degree 2-n)
  satisfying generalized Jacobi identities:
  sum_{i+j=n+1} sum_{sigma} (-1)^sigma (-1)^{i(j-1)} l_j(l_i(x_{sigma(1)},...), ...) = 0.
  For n=1: l_1^2 = 0 (differential); for n=2: Jacobi up to homotopy controlled by l_3.
  L_infty-algebras are the algebras over L_infty = Omega(Lie^!) = Omega(Com).
  Applications: formal deformation theory (Maurer-Cartan elements of a dgla control
  deformations), derived geometry (L_infty-algebras arise as tangent complexes of
  derived stacks), and string field theory (L_infty-structure on closed string field theory).
  Homotopy transfer: L_infty structures transfer along homotopy retracts; every
  dg-Lie algebra is L_infty-quasi-isomorphic to its cohomology (with transferred structure).

- Little disks operads E_n (Boardman-Vogt, May): the little n-disks operad D_n has
  D_n(k) = space of k disjoint little n-dimensional disks embedded in the unit disk D^n
  (rectilinearly, i.e., affine embeddings). Composition: substitute little disks into disks.
  E_n = D_n as an operad in topological spaces (or its homology H_*(D_n)).
  Key properties:
  - E_1 ≃ Ass (over k, H_*(E_1) ≅ Ass): E_1-algebras ≅ A_infty-algebras.
  - E_infty = colim_n E_n ≃ Com (over k): E_infty-algebras ≅ strictly commutative algebras up to homotopy.
  - E_n-algebras interpolate between A_infty (n=1) and E_infty (n=infty).
  Cohomology of configuration spaces: H_*(D_n(k)) = Poisson_n(k) operad (for n>=2).
  Recognition principle (May): a connected (n-1)-fold loop space Omega^{n-1}X is
  group-complete E_n-algebra. The delooping machine: E_n-algebras are equivalent (up
  to group completion) to n-fold loop spaces.
  Factorization homology (Ayala-Francis): for M an n-manifold, the factorization homology
  int_M A of an E_n-algebra A is a topological invariant, generalizing Hochschild homology.
"""

