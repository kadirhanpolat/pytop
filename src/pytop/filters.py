def _finite_space_data(space: Any, operator: str) -> tuple[frozenset[Any], tuple[frozenset[Any], ...]] | Result:
    """Extract carrier and topology from an explicit finite topological space."""
    try:
        carrier = tuple(getattr(space, "carrier"))
        topology = tuple(frozenset(s) for s in getattr(space, "topology"))
    except Exception:
        return Result.unknown(
            mode="symbolic",
            value=operator,
            justification=["Exact filter diagnostics are implemented only for explicit finite topological spaces."],
            metadata={"operator": operator},
        )
    return frozenset(carrier), topology


def _powerset(s: frozenset[Any]) -> tuple[frozenset[Any], ...]:
    """Return all subsets of *s* as frozensets, including the empty set."""
    elements = list(s)
    n = len(elements)
    result: list[frozenset[Any]] = []
    for mask in range(1 << n):
        subset = frozenset(elements[i] for i in range(n) if mask & (1 << i))
        result.append(subset)
    return tuple(sorted(result, key=lambda x: (len(x), tuple(sorted(map(repr, x))))))


