"""Noncommutative topology: C*-algebras, Gelfand duality, K-theory, spectral triples,
and the noncommutative geometry programme of Connes.

Key theorems implemented
------------------------
- Gelfand duality (1943): there is a contravariant equivalence of categories between
  compact Hausdorff spaces and commutative unital C*-algebras, given by X |-> C(X)
  (continuous complex-valued functions) and A |-> Spec(A) (maximal ideal space /
  character space). Under this duality: points of X <-> characters chi: A -> C;
  open sets of X <-> closed ideals of C(X); continuous maps f: X -> Y <->
  *-homomorphisms f*: C(Y) -> C(X). A noncommutative C*-algebra is thus a
  'noncommutative topological space'. Key examples: C(S^1) = commutative (S^1 is
  classical); M_n(C) = noncommutative (n x n matrices, no classical Gelfand dual).
- Gelfand-Naimark theorem (1943): every C*-algebra A is isometrically *-isomorphic
  to a closed *-subalgebra of B(H), the bounded operators on some Hilbert space H.
  Commutative case: A ≅ C_0(X) for locally compact Hausdorff X = Spec(A). This
  justifies calling noncommutative C*-algebras 'noncommutative spaces'.
- K-theory for C*-algebras (Atiyah 1964 / Hirzebruch): for a C*-algebra A, K_0(A)
  is the Grothendieck group of Murray-von Neumann equivalence classes of projections
  in M_infty(A). K_1(A) = K_0(SA) where SA = C_0((0,1)) tensor A is the suspension.
  The six-term exact sequence and Bott periodicity K_{n+2}(A) ≅ K_n(A) are the
  main computational tools. For commutative A = C(X): K_0(C(X)) = K^0(X) (topological
  K-theory), K_1(C(X)) = K^1(X) (Atiyah-Hirzebruch). Key example: K_0(M_n(C)) = Z,
  K_1(M_n(C)) = 0 (matrix algebras).
- Connes' spectral triple (A, H, D) (1994): a spectral triple (also called an
  unbounded Fredholm module) consists of a *-algebra A represented on a Hilbert space
  H, and a self-adjoint operator D ('Dirac operator') with compact resolvent, such
  that [D, a] is bounded for all a in A. The spectral triple encodes a noncommutative
  Riemannian geometry: the metric on the state space S(A) is d(phi, psi) =
  sup{|phi(a) - psi(a)| : ||[D,a]|| <= 1}. For commutative A = C^infty(M): take
  H = L^2(M, S) (spinor bundle), D = Dirac operator — recovers classical Riemannian
  geometry. The spectral triple is even (graded) if H has a Z/2Z grading chi with
  chi·D = -D·chi, modelling the chirality of the Dirac operator.
- Morita equivalence: two C*-algebras A and B are Morita equivalent if there is an
  A-B imprimitivity bimodule — a full Hilbert bimodule. Morita equivalent algebras
  have the same K-theory and representation theory. Key fact: M_n(A) is Morita
  equivalent to A for all n >= 1 (the Morita invariance of K-theory). The
  noncommutative torus A_theta is Morita equivalent to A_{theta'} when theta and
  theta' are related by a modular transformation in PSL(2,Z).
- Noncommutative torus A_theta (Rieffel 1981): for theta in [0,1], the noncommutative
  torus A_theta is the universal C*-algebra generated by unitaries U, V with
  VU = e^{2 pi i theta} UV. For theta = 0: A_0 = C(T^2) (commutative). For theta
  irrational: A_theta is a simple C*-algebra. K_0(A_theta) = Z^2, K_1(A_theta) = Z^2
  (same as C(T^2) by Pimsner-Voiculescu). The noncommutative torus is the paradigm
  example of a noncommutative manifold and models the quantum Hall effect.
- Nuclear C*-algebras: A is nuclear if there is a unique C*-cross-norm on A tensor B
  for every C*-algebra B (equivalently, the minimal and maximal tensor products agree).
  Commutative C*-algebras, AF-algebras, and the CAR algebra are nuclear. The Connes
  embedding problem (resolved negatively, Ji-Natarajan-Vidick-Wright-Yuen 2020):
  not every II_1 factor embeds in the ultrapower of the hyperfinite II_1 factor R.
"""

