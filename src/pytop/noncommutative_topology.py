def has_gelfand_dual(space: Any) -> Result:
    """Check whether the C*-algebra has a classical Gelfand dual (is commutative).

    By Gelfand duality, a C*-algebra A has a classical Hausdorff space as its
    'noncommutative space' iff A is commutative. The Gelfand dual is Spec(A) = the
    space of characters (unital *-homomorphisms A -> C). For noncommutative A, Spec(A)
    is empty or trivial, and the 'space' is purely virtual (noncommutative geometry).

    Decision layers
    ---------------
    1. Explicit 'gelfand_dual' or commutative tags -> true.
    2. Noncommutative or no-Gelfand tags -> false.
    3. Unknown otherwise.
    """
    tags = _extract_tags(space)
    representation = _representation_of(space)
    base: dict[str, Any] = {"representation": representation, "tags": sorted(tags)}

