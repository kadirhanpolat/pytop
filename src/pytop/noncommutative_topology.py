def noncommutative_layer_summary() -> dict[str, int]:
    """Return a count of profiles by presentation_layer."""
    return dict(Counter(
        p.presentation_layer for p in get_named_noncommutative_profiles()
    ))


def noncommutative_chapter_index() -> dict[str, tuple[str, ...]]:
    """Return a mapping from chapter number to profile keys."""
    chapter_map: dict[str, list[str]] = {}
    for p in get_named_noncommutative_profiles():
        for chapter in p.chapter_targets:
            chapter_map.setdefault(chapter, []).append(p.key)
    return {chapter: tuple(keys) for chapter, keys in sorted(chapter_map.items())}


def noncommutative_type_index() -> dict[str, tuple[str, ...]]:
    """Return a mapping from algebra_type to profile keys."""
    index: dict[str, list[str]] = {}
    for p in get_named_noncommutative_profiles():
        index.setdefault(p.algebra_type, []).append(p.key)
    return {k: tuple(v) for k, v in sorted(index.items())}


