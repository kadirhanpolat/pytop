# π₃(S²)
# ---------------------------------------------------------------------------


def pi3_s2() -> dict[str, object]:
    """Return the computation of π₃(S²) = ℤ.

    The third homotopy group of S² is generated by the Hopf fibration
    η₂: S³ → S² with Hopf invariant H(η₂) = 1.

    Every element of π₃(S²) is of the form n·[η₂] with Hopf invariant n².

    Wait: actually π₃(S²) ≅ ℤ and the Hopf invariant H: π₃(S²) → ℤ is an
    isomorphism.  If [f] = n·[η₂] in π₃(S²), then H(f) = n.
    """
    eta2 = hopf_fibration(2)
    return {
        "group": "ℤ",
        "generator": eta2,
        "hopf_invariant_isomorphism": "H: π₃(S²) → ℤ is an isomorphism",
        "generator_name": "η₂ (Hopf fibration)",
        "hopf_invariant_of_generator": 1,
        "description": (
            "π₃(S²) = ℤ generated by the Hopf fibration η₂: S³ → S². "
            "The Hopf invariant gives a bijection π₃(S²) ≅ ℤ. "
            "Discovered by Hopf (1931), this was the first example of a "
            "non-trivial map between spheres of the same homotopy-theoretic type "
            "S^{2n-1} → S^n for n ≥ 2."
        ),
    }


# ---------------------------------------------------------------------------
# Composition formula
# ---------------------------------------------------------------------------


def hopf_invariant_composition(
    hopf_f: int,
    degree_g: int,
) -> int:
    """Hopf invariant of f ∘ g under suitable conditions.

    If f: S^{2n-1} → S^n has Hopf invariant H(f) and
    g: S^{4n-1} → S^{2n-1} has degree deg(g), then
    H(f ∘ g) = deg(g)² · H(f).

    Parameters
    ----------
    hopf_f : int
        Hopf invariant of f.
    degree_g : int
        Degree of g (viewed as a self-map of a sphere).
    """
    return degree_g * degree_g * hopf_f


def hopf_invariant_sum(hopf_f1: int, hopf_f2: int) -> int:
    """Hopf invariant of the sum f₁ + f₂ (in the stable range).

    In π^s_{4n-1}(S^{2n}) the Hopf invariant is additive:
    H(f₁ + f₂) = H(f₁) + H(f₂).
    """
    return hopf_f1 + hopf_f2


def hopf_invariant_degree_map(n: int, degree: int) -> int:
    """Hopf invariant of the degree-k map S^{2n-1} → S^{2n-1} → S^n.

    For f = η_n (Hopf fibration) and g = [k] (degree-k map of S^{2n-1}):
    H(η_n ∘ [k]) = k² · H(η_n) = k².

    Parameters
    ----------
    n : int
        Dimension of target sphere (must be 1, 2, 4, or 8 for η_n).
    degree : int
        Degree of the pre-composed map.
    """
    if not adams_hopf_invariant_one(n):
        raise ValueError(
            f"Hopf fibration η_n exists only for n=1,2,4,8; got n={n}."
        )
    return degree * degree
