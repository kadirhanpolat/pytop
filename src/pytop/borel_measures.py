"""Borel measures, Radon measures, regularity, and Riesz representation.

Key theorems implemented
------------------------
- A Borel measure on a topological space X is a measure defined on the Borel
  sigma-algebra B(X) generated by the open sets of X.
- A Radon measure is a Borel measure that is locally finite (every point has a
  neighbourhood of finite measure) and inner regular (mu(A) = sup{mu(K) : K compact, K subset A}).
- Riesz-Markov-Kakutani representation theorem: for a compact Hausdorff space X,
  every positive linear functional L on C(X) is represented by a unique regular
  Borel measure mu such that L(f) = integral f d-mu.
- Regularity: a Borel measure is outer regular if mu(A) = inf{mu(U) : U open, A subset U}
  and inner regular if mu(A) = sup{mu(K) : K compact, K subset A}.
- Haar measure: every locally compact topological group G admits a left-invariant
  Radon measure (Haar measure), unique up to a positive scalar multiple.
- On compact groups, the Haar measure can be normalised to a probability measure.
- The Lebesgue measure on R^n is translation-invariant, regular, non-atomic, and sigma-finite.
- The Dirac measure delta_x is a probability measure supported at {x}; it is atomic and Radon.
- The Cantor measure (Devil's staircase measure) is supported on the Cantor set,
  is non-atomic, and is singular with respect to the Lebesgue measure.
- On a locally compact Hausdorff space, every Radon measure is regular.
- The counting measure on an uncountable discrete space is NOT locally finite, hence not Radon.
"""

