def is_radon_measure(space: Any) -> Result:
    """Check whether the measure on space is a Radon measure.

    A Radon measure on a topological space X is a Borel measure mu that is:
    (i)  locally finite: every point x has a neighbourhood U with mu(U) < inf,
    (ii) inner regular: mu(A) = sup{mu(K) : K subset A, K compact} for all Borel A.
    Key facts:
    - On a compact Hausdorff space, every finite Borel measure is Radon.
    - On a locally compact Hausdorff space, every regular Borel measure is Radon.
    - The Lebesgue, Dirac, Haar, and Gaussian measures are all Radon.
    - The counting measure on an uncountable discrete space is NOT Radon
      (fails local finiteness: every open set {x} has measure 1, but R is uncountable).

    Decision layers
    ---------------
    1. Explicit Radon / Dirac / Lebesgue / Haar / Gaussian tag -> true.
    2. Compact Hausdorff space (finite Borel measures are Radon) -> true.
    3. Locally compact Hausdorff + regular Borel measure -> true.
    4. Counting measure on uncountable space -> false.
    5. Explicit not-Radon tag -> false.
    6. Unknown otherwise.
    """
    tags = _extract_tags(space)
    representation = _representation_of(space)
    base: dict[str, Any] = {"representation": representation, "tags": sorted(tags)}

    if _matches_any(tags, RADON_MEASURE_TAGS):
        witness = next(t for t in tags if t in RADON_MEASURE_TAGS)
        return Result.true(
            mode="theorem",
            value="radon_measure",
            justification=[
                f"Tag {witness!r}: this is a Radon measure — locally finite and "
                "inner regular. Examples include Lebesgue, Dirac, Haar, and Gaussian measures.",
            ],
            metadata={**base, "criterion": "radon_tag", "witness": witness},
        )

    if _matches_any(tags, COMPACT_HAUSDORFF_SPACE_TAGS):
        witness = next(t for t in tags if t in COMPACT_HAUSDORFF_SPACE_TAGS)
        return Result.true(
            mode="theorem",
            value="radon_measure",
            justification=[
                f"Tag {witness!r}: on a compact Hausdorff space, every finite Borel "
                "measure is Radon. The space is compact, so local finiteness is automatic, "
                "and inner regularity follows from regularity of Borel measures.",
            ],
            metadata={**base, "criterion": "compact_hausdorff_radon", "witness": witness},
        )

    if (_matches_any(tags, LOCALLY_COMPACT_HAUSDORFF_TAGS) and
            _matches_any(tags, REGULAR_MEASURE_TAGS)):
        witness = next(t for t in tags if t in LOCALLY_COMPACT_HAUSDORFF_TAGS)
        return Result.true(
            mode="theorem",
            value="radon_measure",
            justification=[
                f"Tag {witness!r}: on a locally compact Hausdorff space, regular Borel "
                "measures are Radon. Local finiteness holds because compact neighbourhoods "
                "have finite measure.",
            ],
            metadata={**base, "criterion": "lc_hausdorff_regular_radon", "witness": witness},
        )

    if _matches_any(tags, NOT_RADON_TAGS | {"counting_measure_uncountable"}):
        blocking = next(t for t in tags if t in (NOT_RADON_TAGS |
                                                   {"counting_measure_uncountable"}))
        return Result.false(
            mode="theorem",
            value="radon_measure",
            justification=[
                f"Tag {blocking!r}: the counting measure on an uncountable space is not "
                "locally finite — every open set {x} has measure 1, but there is no "
                "neighbourhood of finite measure. Hence it is not a Radon measure.",
            ],
            metadata={**base, "criterion": "not_radon"},
        )

    return Result.unknown(
        mode="symbolic",
        value="radon_measure",
        justification=[
            "Insufficient tags to determine whether the measure is Radon. "
            "Supply tags such as 'radon_measure', 'lebesgue_measure', 'dirac_measure', "
            "'compact_hausdorff', 'locally_compact_hausdorff', or 'counting_measure_uncountable'.",
        ],
        metadata={**base, "criterion": None},
    )


def is_regular_measure(space: Any) -> Result:
    """Check whether the measure on space is regular (outer and inner regular).

    A Borel measure mu on a topological space X is:
    - Outer regular: mu(A) = inf{mu(U) : U open, A subset U} for all Borel A.
    - Inner regular: mu(A) = sup{mu(K) : K compact, K subset A} for all Borel A.
    - Regular: both outer and inner regular.
    Key facts:
    - On a locally compact Hausdorff space, every Radon measure is regular.
    - The Lebesgue, Dirac, Haar, and Gaussian measures are all regular.
    - On a compact metric space, every Borel probability measure is regular
      (Prokhorov / Ulam's theorem).

    Decision layers
    ---------------
    1. Explicit regular measure tags -> true.
    2. Radon measure on locally compact Hausdorff space -> true.
    3. Compact metric space (Ulam's theorem) -> true.
    4. Explicit not-regular tag -> false.
    5. Unknown otherwise.
    """
    tags = _extract_tags(space)
    representation = _representation_of(space)
    base: dict[str, Any] = {"representation": representation, "tags": sorted(tags)}

    if _matches_any(tags, REGULAR_MEASURE_TAGS):
        witness = next(t for t in tags if t in REGULAR_MEASURE_TAGS)
        return Result.true(
            mode="theorem",
            value="regular_measure",
            justification=[
                f"Tag {witness!r}: the measure is regular — both outer regular "
                "(approximated from outside by open sets) and inner regular "
                "(approximated from inside by compact sets).",
            ],
            metadata={**base, "criterion": "regular_tag", "witness": witness},
        )

    if (_matches_any(tags, RADON_MEASURE_TAGS) and
            _matches_any(tags, LOCALLY_COMPACT_HAUSDORFF_TAGS)):
        witness = next(t for t in tags if t in RADON_MEASURE_TAGS)
        return Result.true(
            mode="theorem",
            value="regular_measure",
            justification=[
                f"Tag {witness!r}: Radon measures on locally compact Hausdorff spaces "
                "are regular. Inner regularity and outer regularity both hold.",
            ],
            metadata={**base, "criterion": "radon_lc_hausdorff_regular", "witness": witness},
        )

    if (_matches_any(tags, {"compact_metrizable", "compact_metric"}) and
            _matches_any(tags, {"probability_measure", "finite_measure",
                                 "borel_measure", "radon_measure"})):
        witness = next(t for t in tags if t in {"compact_metrizable", "compact_metric"})
        return Result.true(
            mode="theorem",
            value="regular_measure",
            justification=[
                f"Tag {witness!r}: by Ulam's theorem (Prokhorov's theorem), every Borel "
                "probability measure on a compact metric space is regular (tight).",
            ],
            metadata={**base, "criterion": "ulam_theorem", "witness": witness},
        )

    if _matches_any(tags, {"not_regular", "not_regular_measure",
                            "outer_regular_not_inner", "inner_regular_not_outer"}):
        blocking = next(t for t in tags if t in {"not_regular", "not_regular_measure",
                                                    "outer_regular_not_inner",
                                                    "inner_regular_not_outer"})
        return Result.false(
            mode="theorem",
            value="regular_measure",
            justification=[
                f"Tag {blocking!r}: the measure fails full regularity — "
                "it is not simultaneously outer and inner regular.",
            ],
            metadata={**base, "criterion": "not_regular"},
        )

    return Result.unknown(
        mode="symbolic",
        value="regular_measure",
        justification=[
            "Insufficient tags to determine regularity. "
            "Supply tags such as 'regular_measure', 'radon_measure', 'lebesgue_measure', "
            "'compact_metrizable', or 'locally_compact_hausdorff'.",
        ],
        metadata={**base, "criterion": None},
    )


def riesz_representation_applies(space: Any) -> Result:
    """Check whether the Riesz-Markov-Kakutani representation theorem applies.

    The Riesz-Markov-Kakutani theorem: for a locally compact Hausdorff space X,
    every positive linear functional L on C_c(X) (continuous functions with compact
    support) is represented by a unique regular Borel measure mu such that
    L(f) = integral f d-mu for all f in C_c(X).
    For compact Hausdorff X: C_c(X) = C(X) and the dual C(X)* is exactly the
    space of signed regular Borel measures (by the full Riesz theorem).
    Key facts:
    - Compact Hausdorff: C(X)* cong M(X) (regular Borel measures).
    - Locally compact Hausdorff: C_c(X) functionals <- regular Borel measures.
    - Non-Hausdorff spaces: the theorem fails in general.

    Decision layers
    ---------------
    1. Compact Hausdorff -> true (full Riesz theorem for C(X)*).
    2. Locally compact Hausdorff -> true (Riesz for C_c(X)).
    3. Non-Hausdorff / not T2 -> false.
    4. Infinite-dimensional without local compactness -> false.
    5. Unknown otherwise.
    """
    tags = _extract_tags(space)
    representation = _representation_of(space)
    base: dict[str, Any] = {"representation": representation, "tags": sorted(tags)}

    if _matches_any(tags, COMPACT_HAUSDORFF_SPACE_TAGS):
        witness = next(t for t in tags if t in COMPACT_HAUSDORFF_SPACE_TAGS)
        return Result.true(
            mode="theorem",
            value="riesz_representation",
            justification=[
                f"Tag {witness!r}: compact Hausdorff space. The Riesz-Markov-Kakutani "
                "theorem applies in its strongest form: C(X)* is isometrically isomorphic "
                "to the space M(X) of signed regular Borel measures on X.",
            ],
            metadata={**base, "criterion": "compact_hausdorff_riesz", "witness": witness},
        )

    if _matches_any(tags, LOCALLY_COMPACT_HAUSDORFF_TAGS):
        witness = next(t for t in tags if t in LOCALLY_COMPACT_HAUSDORFF_TAGS)
        return Result.true(
            mode="theorem",
            value="riesz_representation",
            justification=[
                f"Tag {witness!r}: locally compact Hausdorff space. The Riesz-Markov-Kakutani "
                "theorem applies: every positive linear functional on C_c(X) (compactly "
                "supported continuous functions) is represented by a unique regular Borel measure.",
            ],
            metadata={**base, "criterion": "lc_hausdorff_riesz", "witness": witness},
        )

    if _matches_any(tags, {"not_hausdorff", "non_hausdorff",
                            "t0_not_t1", "not_t2"}):
        blocking = next(t for t in tags if t in {"not_hausdorff", "non_hausdorff",
                                                    "t0_not_t1", "not_t2"})
        return Result.false(
            mode="theorem",
            value="riesz_representation",
            justification=[
                f"Tag {blocking!r}: the Riesz-Markov-Kakutani theorem requires the "
                "Hausdorff (T2) separation axiom. Non-Hausdorff spaces do not generally "
                "admit a representation by regular Borel measures.",
            ],
            metadata={**base, "criterion": "not_hausdorff"},
        )

    if _matches_any(tags, NOT_HAAR_TAGS):
        blocking = next(t for t in tags if t in NOT_HAAR_TAGS)
        return Result.false(
            mode="theorem",
            value="riesz_representation",
            justification=[
                f"Tag {blocking!r}: infinite-dimensional spaces without local compactness "
                "(e.g., infinite-dimensional Banach or Hilbert spaces) do not satisfy "
                "the hypotheses of the Riesz theorem. The C_c(X) duality requires "
                "local compactness.",
            ],
            metadata={**base, "criterion": "not_locally_compact_riesz"},
        )

    return Result.unknown(
        mode="symbolic",
        value="riesz_representation",
        justification=[
            "Insufficient tags to determine Riesz representation applicability. "
            "Supply tags such as 'compact_hausdorff', 'locally_compact_hausdorff', "
            "'real_line', 'not_hausdorff', or 'infinite_dimensional_banach'.",
        ],
        metadata={**base, "criterion": None},
    )


def has_haar_measure(space: Any) -> Result:
    """Check whether space (as a topological group) admits a Haar measure.

    Every locally compact topological group G admits a left Haar measure: a
    non-zero left-invariant Radon measure, unique up to a positive scalar multiple.
    Key facts:
    - Compact groups: normalised Haar measure is also a probability measure.
    - Discrete groups: Haar measure is the counting measure.
    - R^n (additive): Haar measure is the Lebesgue measure.
    - Infinite-dimensional topological groups (e.g., Hilbert spaces) generally
      do NOT admit a Haar measure (no sigma-finite translation-invariant measure
      exists on infinite-dimensional Banach spaces).

    Decision layers
    ---------------
    1. Explicit Haar measure tag -> true.
    2. Locally compact topological group (including compact groups) -> true.
    3. Discrete group (counting measure is Haar) -> true.
    4. Locally compact abelian group -> true.
    5. Infinite-dimensional Banach / Hilbert / Frechet (not locally compact) -> false.
    6. Unknown otherwise.
    """
    tags = _extract_tags(space)
    representation = _representation_of(space)
    base: dict[str, Any] = {"representation": representation, "tags": sorted(tags)}

    if _matches_any(tags, {"haar_measure"}):
        return Result.true(
            mode="theorem",
            value="haar_measure",
            justification=[
                "Tag 'haar_measure': the space is equipped with an explicit Haar measure "
                "— a left-invariant Radon measure, unique up to positive scalar multiple.",
            ],
            metadata={**base, "criterion": "explicit_haar"},
        )

    if _matches_any(tags, HAAR_MEASURE_TAGS):
        witness = next(t for t in tags if t in HAAR_MEASURE_TAGS)
        return Result.true(
            mode="theorem",
            value="haar_measure",
            justification=[
                f"Tag {witness!r}: locally compact topological group. "
                "By the Haar measure theorem, every locally compact group admits "
                "a left-invariant Radon measure, unique up to a positive scalar. "
                "For compact groups it can be normalised to a probability measure.",
            ],
            metadata={**base, "criterion": "locally_compact_group_haar", "witness": witness},
        )

    if _matches_any(tags, {"discrete_group", "discrete", "countable_discrete"}):
        witness = next(t for t in tags if t in {"discrete_group", "discrete",
                                                   "countable_discrete"})
        return Result.true(
            mode="theorem",
            value="haar_measure",
            justification=[
                f"Tag {witness!r}: discrete groups are locally compact. "
                "The counting measure is the Haar measure for any discrete group. "
                "It is left-invariant (translations are bijections on the group).",
            ],
            metadata={**base, "criterion": "discrete_group_counting_haar", "witness": witness},
        )

    if _matches_any(tags, NOT_HAAR_TAGS):
        blocking = next(t for t in tags if t in NOT_HAAR_TAGS)
        return Result.false(
            mode="theorem",
            value="haar_measure",
            justification=[
                f"Tag {blocking!r}: infinite-dimensional Banach and Hilbert spaces are "
                "NOT locally compact. No sigma-finite translation-invariant Borel measure "
                "exists on an infinite-dimensional locally convex space "
                "(Weil's theorem / non-existence result).",
            ],
            metadata={**base, "criterion": "no_haar_infinite_dim"},
        )

    return Result.unknown(
        mode="symbolic",
        value="haar_measure",
        justification=[
            "Insufficient tags to determine Haar measure existence. "
            "Supply tags such as 'topological_group', 'locally_compact_group', "
            "'compact_group', 'discrete_group', 'real_line', "
            "or 'infinite_dimensional_banach'.",
        ],
        metadata={**base, "criterion": None},
    )


def measure_support_is_compact(space: Any) -> Result:
    """Check whether the support of the measure is compact.

    The support of a Borel measure mu on X is the smallest closed set F such that
    mu(X \\ F) = 0. Equivalently, x is in supp(mu) iff every open neighbourhood
    of x has positive measure. Key facts:
    - Dirac measure delta_x: support = {x} (compact).
    - Measure on a compact space: support subset of compact space -> compact.
    - Lebesgue measure on R: support = R (not compact).
    - Gaussian measure on R^n: support = R^n (not compact).
    - Cantor measure: support = Cantor set C (compact).
    - Counting measure on Z: support = Z (not compact).

    Decision layers
    ---------------
    1. Dirac measure (support = singleton) -> true.
    2. Measure on compact space (support subset compact) -> true.
    3. Compactly supported measure tag -> true.
    4. Cantor measure (support = Cantor set, compact) -> true.
    5. Lebesgue / Gaussian / Haar on R^n (support = R^n, not compact) -> false.
    6. Unknown otherwise.
    """
    tags = _extract_tags(space)
    representation = _representation_of(space)
    base: dict[str, Any] = {"representation": representation, "tags": sorted(tags)}

    if _matches_any(tags, {"dirac_measure", "dirac", "point_mass"}):
        witness = next(t for t in tags if t in {"dirac_measure", "dirac", "point_mass"})
        return Result.true(
            mode="theorem",
            value="support_compact",
            justification=[
                f"Tag {witness!r}: the Dirac measure delta_x has support = {{x}}, "
                "a singleton — which is compact in any Hausdorff space.",
            ],
            metadata={**base, "criterion": "dirac_singleton_support", "witness": witness},
        )

    if _matches_any(tags, COMPACT_SUPPORT_TAGS):
        witness = next(t for t in tags if t in COMPACT_SUPPORT_TAGS)
        return Result.true(
            mode="theorem",
            value="support_compact",
            justification=[
                f"Tag {witness!r}: the measure has compact support. "
                "Any closed subset of a compact space is compact.",
            ],
            metadata={**base, "criterion": "compact_support_tag", "witness": witness},
        )

    if _matches_any(tags, COMPACT_HAUSDORFF_SPACE_TAGS):
        witness = next(t for t in tags if t in COMPACT_HAUSDORFF_SPACE_TAGS)
        return Result.true(
            mode="theorem",
            value="support_compact",
            justification=[
                f"Tag {witness!r}: the underlying space is compact. "
                "The support of any Borel measure on a compact space is a closed subset "
                "of a compact space, hence compact.",
            ],
            metadata={**base, "criterion": "compact_space_support_compact", "witness": witness},
        )

    if _matches_any(tags, {"cantor_measure"}):
        return Result.true(
            mode="theorem",
            value="support_compact",
            justification=[
                "Tag 'cantor_measure': the Cantor measure is supported on the Cantor set C, "
                "which is compact (closed and bounded in [0,1]).",
            ],
            metadata={**base, "criterion": "cantor_measure_compact_support"},
        )

    if _matches_any(tags, {"lebesgue_measure", "gaussian_measure",
                            "haar_measure_lc", "counting_measure"}):
        blocking = next(t for t in tags if t in {"lebesgue_measure", "gaussian_measure",
                                                    "haar_measure_lc", "counting_measure"})
        return Result.false(
            mode="theorem",
            value="support_compact",
            justification=[
                f"Tag {blocking!r}: the Lebesgue measure on R^n, Gaussian measure, "
                "and Haar measure on non-compact groups have support equal to the whole "
                "space (or a non-compact closed subset), which is not compact.",
            ],
            metadata={**base, "criterion": "non_compact_support"},
        )

    return Result.unknown(
        mode="symbolic",
        value="support_compact",
        justification=[
            "Insufficient tags to determine compactness of the measure support. "
            "Supply tags such as 'dirac_measure', 'compact_hausdorff', "
            "'cantor_measure', 'lebesgue_measure', or 'compact_support'.",
        ],
        metadata={**base, "criterion": None},
    )


def classify_borel_measure(space: Any) -> dict[str, Any]:
    """Classify the Borel measure properties of space.

    Keys
    ----
    measure_class : str
        One of ``"radon_regular"``, ``"radon"``, ``"regular"``,
        ``"finite_borel"``, ``"borel"``, ``"not_radon"``, ``"unknown"``.
    is_radon : Result
    is_regular : Result
    riesz_applicable : Result
    has_haar : Result
    support_compact : Result
    key_properties : list[str]
    representation : str
    tags : list[str]
    """
    tags = _extract_tags(space)
    representation = _representation_of(space)

    radon_r = is_radon_measure(space)
    reg_r = is_regular_measure(space)
    riesz_r = riesz_representation_applies(space)
    haar_r = has_haar_measure(space)
    supp_r = measure_support_is_compact(space)

    if radon_r.is_true and reg_r.is_true:
        measure_class = "radon_regular"
    elif radon_r.is_true:
        measure_class = "radon"
    elif reg_r.is_true:
        measure_class = "regular"
    elif _matches_any(tags, {"finite_measure", "probability_measure"}):
        measure_class = "finite_borel"
    elif radon_r.is_false:
        measure_class = "not_radon"
    else:
        measure_class = "unknown"

    key_properties: list[str] = []
    if radon_r.is_true:
        key_properties.append("radon")
    if reg_r.is_true:
        key_properties.append("regular")
    if riesz_r.is_true:
        key_properties.append("riesz_applicable")
    if haar_r.is_true:
        key_properties.append("haar_measure")
    if supp_r.is_true:
        key_properties.append("compact_support")
    if supp_r.is_false:
        key_properties.append("non_compact_support")
    if _matches_any(tags, ATOMIC_MEASURE_TAGS):
        key_properties.append("atomic")
    if _matches_any(tags, NON_ATOMIC_TAGS):
        key_properties.append("non_atomic")
    if radon_r.is_false:
        key_properties.append("not_radon")

    return {
        "measure_class": measure_class,
        "is_radon": radon_r,
        "is_regular": reg_r,
        "riesz_applicable": riesz_r,
        "has_haar": haar_r,
        "support_compact": supp_r,
        "key_properties": key_properties,
        "representation": representation,
        "tags": sorted(tags),
    }


def borel_measure_profile(space: Any) -> dict[str, Any]:
    """Full Borel measure profile combining classification and named examples.

    Keys
    ----
    classification : dict
        Output of :func:`classify_borel_measure`.
    named_profiles : tuple[BorelMeasureProfile, ...]
        Registry of canonical Borel measure examples.
    layer_summary : dict[str, int]
        Profile count by presentation_layer.
    """
    return {
        "classification": classify_borel_measure(space),
        "named_profiles": get_named_borel_measure_profiles(),
        "layer_summary": borel_measure_layer_summary(),
    }


