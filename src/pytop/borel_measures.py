def borel_measure_layer_summary() -> dict[str, int]:
    """Return a count of profiles by presentation_layer."""
    return dict(Counter(p.presentation_layer for p in get_named_borel_measure_profiles()))


def borel_measure_chapter_index() -> dict[str, tuple[str, ...]]:
    """Return a mapping from chapter number to profile keys."""
    chapter_map: dict[str, list[str]] = {}
    for p in get_named_borel_measure_profiles():
        for chapter in p.chapter_targets:
            chapter_map.setdefault(chapter, []).append(p.key)
    return {chapter: tuple(keys) for chapter, keys in sorted(chapter_map.items())}


def borel_measure_type_index() -> dict[str, tuple[str, ...]]:
    """Return a mapping from measure_type to profile keys."""
    index: dict[str, list[str]] = {}
    for p in get_named_borel_measure_profiles():
        index.setdefault(p.measure_type, []).append(p.key)
    return {k: tuple(v) for k, v in sorted(index.items())}


